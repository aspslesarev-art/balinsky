// Editable knowledge base for Балина.
//
// The full SYSTEM_PROMPT used to live as one big template literal in
// lib/consultant.ts. That made it impossible to tweak rules without
// a deploy. This module:
//
//   1. Defines the canonical DEFAULT_SECTIONS — the seed copy of every
//      named block in the prompt. Code is the source of truth for
//      "what sections exist"; DB is the source of truth for "what's
//      currently in each section's body".
//   2. Loads the live bodies from public.assistant_knowledge (migr 017).
//      First load also seeds any missing sections so a fresh DB just
//      works without a manual setup step.
//   3. Renders the assembled prompt by concatenating bodies in
//      sort_order. Cached in-memory for 30s so a busy chat doesn't
//      hammer the DB on every request.
//   4. Exposes admin helpers — list / update / reset — used by the
//      /admin/balina UI.
//
// Cache invalidation: any successful updateSection() bumps the
// in-process version, so the next prompt load re-fetches. The 30s
// TTL is the upper bound when the writer was a different instance.

