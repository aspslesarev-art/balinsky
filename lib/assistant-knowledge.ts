// In-process cache. Bumped by writes; expires after CACHE_TTL_MS so
// a writer in another instance is picked up within that window.
const CACHE_TTL_MS = 30_000
let cachedSections: KnowledgeSection[] | null = null
let cachedAt = 0

function invalidateCache() {
  cachedSections = null
  cachedAt = 0
}

// True when the migration hasn't been applied yet — the page falls
// back to read-only defaults from code, the chat falls back to
// renderPrompt(DEFAULT_SECTIONS), and the admin UI shows a banner.
let tableMissing = false
export function isKnowledgeTableMissing(): boolean { return tableMissing }

function defaultSectionsAsLoaded(): KnowledgeSection[] {
  return DEFAULT_SECTIONS.map(s => ({
    key: s.key,
    title: s.title,
    body: s.body,
    sortOrder: s.sortOrder,
    isDefault: true,
    updatedAt: null,
  }))
}

// First-load seeding: any DEFAULT_SECTIONS key missing in DB gets
// inserted with the default body. Existing rows are NEVER overwritten
// — that's the admin's edit. Returns the loaded sections. If the
// table itself doesn't exist (migration not applied), returns the
// in-code defaults so the app keeps working — the admin page shows
// a "migration not applied" banner.
async function loadAndSeed(): Promise<KnowledgeSection[]> {
  const first = await sb
    .from('assistant_knowledge')
    .select('key, title, body, sort_order, is_default, updated_at')
    .order('sort_order', { ascending: true })

  if (first.error) {
    // Most common: relation does not exist (migration not applied).
    // Treat any error here as "table missing" so the admin UI shows
    // the migration banner instead of a blank page.
    console.error('[balina] select failed, falling back to defaults:', first.error)
    tableMissing = true
    return defaultSectionsAsLoaded()
  }
  tableMissing = false

  const rows = first.data ?? []
  const existingKeys = new Set(rows.map(r => r.key))
  const missing = DEFAULT_SECTIONS.filter(s => !existingKeys.has(s.key))

  if (missing.length === 0) {
    return rows.map(toSection)
  }

  // Seed missing sections. Important: surface the insert error so we
  // don't silently fall through to an empty re-fetch and render zero
  // sections (the bug you hit when the table existed but seeding got
  // blocked).
  const insertRows = missing.map(s => ({
    key: s.key,
    title: s.title,
    body: s.body,
    sort_order: s.sortOrder,
    is_default: true,
  }))
  const ins = await sb.from('assistant_knowledge').insert(insertRows)
  if (ins.error) {
    console.error('[balina] seed insert failed, falling back to defaults:', ins.error)
    tableMissing = true
    return defaultSectionsAsLoaded()
  }

  const refetch = await sb
    .from('assistant_knowledge')
    .select('key, title, body, sort_order, is_default, updated_at')
    .order('sort_order', { ascending: true })

  if (refetch.error || !refetch.data || refetch.data.length === 0) {
    console.error('[balina] re-fetch after seed returned nothing:', refetch.error)
    tableMissing = true
    return defaultSectionsAsLoaded()
  }
  return refetch.data.map(toSection)
}

function toSection(r: {
  key: string; title: string; body: string;
  sort_order: number; is_default: boolean | null; updated_at: string | null;
}): KnowledgeSection {
  return {
    key: r.key, title: r.title, body: r.body,
    sortOrder: r.sort_order, isDefault: !!r.is_default,
    updatedAt: r.updated_at ?? null,
  }
}

export async function loadKnowledgeSections(): Promise<KnowledgeSection[]> {
  const now = Date.now()
  if (cachedSections && now - cachedAt < CACHE_TTL_MS) return cachedSections
  cachedSections = await loadAndSeed()
  cachedAt = now
  return cachedSections
}

// Render the full system prompt by concatenating section bodies
// in sort order, prefixing each with its TITLE so the model still
// sees the structured shape it expects. Sections with the same
// position-in-prompt that no longer have a title (e.g. INTRO) just
// emit their body as the lead paragraph.
function renderPrompt(sections: KnowledgeSection[]): string {
  return sections
    .map(s => {
      if (s.key === 'intro') return s.body
      return `${s.title}:\n${s.body}`
    })
    .join('\n\n')
}

export async function getSystemPrompt(): Promise<string> {
  const sections = await loadKnowledgeSections()
  return renderPrompt(sections)
}

// Admin: update one section's body. Sets is_default=false because
// the body has diverged from the seed. Bumps in-process cache so
// the next chat picks it up immediately.
export async function updateSection(key: string, body: string): Promise<void> {
  if (tableMissing) throw new Error('migration_not_applied')
  const seed = DEFAULT_SECTIONS.find(s => s.key === key)
  const isDefault = seed != null && seed.body === body
  const { error } = await sb
    .from('assistant_knowledge')
    .update({ body, is_default: isDefault, updated_at: new Date().toISOString() })
    .eq('key', key)
  if (error) throw error
  invalidateCache()
}

// Append a learned-rule line to the `learned_rules` section.
// Triggered from Telegram when the owner says "слушай и запоминай: …".
// Stored as a dated bullet so the owner can audit + delete rules
// from /admin/balina later. Caps at LEARNED_RULES_CAP total bullets
// (oldest dropped) so the section can't grow unbounded.
const LEARNED_RULES_CAP = 50

export async function appendLearnedRule(rule: string): Promise<void> {
  if (tableMissing) throw new Error('migration_not_applied')
  const trimmed = rule.trim()
  if (!trimmed) return

  const sections = await loadKnowledgeSections()
  const current = sections.find(s => s.key === 'learned_rules')
  const seed = DEFAULT_SECTIONS.find(s => s.key === 'learned_rules')

  // Strip the placeholder "(Пока пусто …)" line on first append.
  const baseBody = current
    ? current.body.replace(/\n*\(Пока пусто[^)]*\)\s*$/i, '').trim()
    : (seed?.body ?? '').replace(/\n*\(Пока пусто[^)]*\)\s*$/i, '').trim()

  const today = new Date().toISOString().slice(0, 10)
  const newLine = `- [${today}] ${trimmed}`

  // Keep the heading sentence + the most recent LEARNED_RULES_CAP
  // bullets. Heading is the first paragraph (everything before the
  // first bullet line), bullets are lines starting with "- ".
  const headingMatch = baseBody.match(/^([\s\S]*?)(?=\n- |\Z)/)
  const heading = headingMatch ? headingMatch[1].trim() : ''
  const bulletLines = baseBody
    .split('\n')
    .filter(l => l.startsWith('- '))
  bulletLines.push(newLine)
  const trimmedBullets = bulletLines.slice(-LEARNED_RULES_CAP)

  const newBody = [heading, '', ...trimmedBullets].filter(Boolean).join('\n')

  const { error } = await sb
    .from('assistant_knowledge')
    .update({ body: newBody, is_default: false, updated_at: new Date().toISOString() })
    .eq('key', 'learned_rules')
  if (error) throw error
  invalidateCache()
}

// Admin: revert one section back to the seed body in DEFAULT_SECTIONS.
// Used by the "Сбросить к умолчанию" button in /admin/balina.
export async function resetSection(key: string): Promise<KnowledgeSection> {
  if (tableMissing) throw new Error('migration_not_applied')
  const seed = DEFAULT_SECTIONS.find(s => s.key === key)
  if (!seed) throw new Error(`unknown_section:${key}`)
  const { error } = await sb
    .from('assistant_knowledge')
    .update({ body: seed.body, is_default: true, updated_at: new Date().toISOString() })
    .eq('key', key)
  if (error) throw error
  invalidateCache()
  return {
    key: seed.key,
    title: seed.title,
    body: seed.body,
    sortOrder: seed.sortOrder,
    isDefault: true,
    updatedAt: new Date().toISOString(),
  }
}
